     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $this->validate($request, [
            'title' => 'required',
            'body' => 'required',
            'bodyTwo' => 'required'
        ]);

        // Create Post
        $admin = Admin::find($id);
        $admin->title = $request->input('title');
        $admin->body = $request->input('body');
        $admin->bodyTwo = $request->input('bodyTwo');
        $admin->save();

        return redirect('/admin')->with('success', 'Section Updated');
    }

